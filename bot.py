@bot.message_handler(commands=["start"])
def start(message):
    markup = InlineKeyboardMarkup()
    for name in DEVELOPERS:
        markup.add(InlineKeyboardButton(name, callback_data=name))
    bot.send_message(message.chat.id, "📊 Індекс репутації забудовників Одеси\n\nОберіть забудовника:", reply_markup=markup)

@bot.callback_query_handler(func=lambda call: call.data == "back")
def go_back(call):
    markup = InlineKeyboardMarkup()
    for name in DEVELOPERS:
        markup.add(InlineKeyboardButton(name, callback_data=name))
    bot.edit_message_text("📊 Індекс репутації забудовників Одеси\n\nОберіть забудовника:", call.message.chat.id, call.message.message_id, reply_markup=markup)

@bot.callback_query_handler(func=lambda call: True)
def handle(call):
    text = DEVELOPERS.get(call.data, "Дані відсутні")
    back_markup = InlineKeyboardMarkup()
    back_markup.add(InlineKeyboardButton("Назад к списку", callback_data="back"))
    bot.edit_message_text(text, call.message.chat.id, call.message.message_id, reply_markup=back_markup)

bot.polling()
